/*
=========================================================
                    Treasure Hunt
=========================================================

■ 프로젝트 소개
Unity 6를 사용하여 제작한 미니 게임입니다.
플레이어는 맵을 탐험하며 숨겨진 상자 4개를 모두 찾는 것이 목표입니다.
모든 상자를 획득하면 출구가 활성화되며,
출구에 도착하면 게임이 클리어됩니다.

---------------------------------------------------------
■ 개발 환경
- Unity 6
- C#
- Input System
- Cinemachine
- TextMeshPro

---------------------------------------------------------
■ 조작 방법

W / A / S / D : 이동
Space          : 점프
1              : 기본 카메라
2              : 원거리 카메라

---------------------------------------------------------
■ 게임 진행

1. 게임 시작
2. 맵을 탐험하며 상자 4개 찾기
3. 상자를 획득하면 UI 카운트 증가
4. 모든 상자를 획득하면 출구 생성
5. 출구에 도착하면 CLEAR 화면 출력

---------------------------------------------------------
■ 구현 기능

- Input System 기반 플레이어 이동
- Character Controller를 이용한 이동 및 점프
- Cinemachine Follow Camera
- 카메라 전환 기능
- Trigger 기반 상자 획득 시스템
- UI(TextMeshPro) 상자 카운트 표시
- 출구 활성화 이벤트
- CLEAR 화면 출력

=========================================================
*/